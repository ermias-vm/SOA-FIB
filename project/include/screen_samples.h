/**
 * @brief Generate a checkerboard pattern in the provided buffer.
 *
 * Creates an alternating A/B pattern with blue/red backgrounds.
 * The pattern alternates both horizontally and vertically.
 *
 * @param buffer Pointer to buffer of exactly SCREEN_BUFFER_SIZE bytes.
 */
void generate_checkerboard_pattern(char *buffer);

/**
 * @brief Generate a rainbow gradient pattern in the provided buffer.
 *
 * Creates a colorful pattern with numbers 0-9 and cycling colors.
 * Each row shifts the color pattern to create a gradient effect.
 *
 * @param buffer Pointer to buffer of exactly SCREEN_BUFFER_SIZE bytes.
 */
void generate_rainbow_pattern(char *buffer);

/**
 * @brief Generate a border pattern in the provided buffer.
 *
 * Creates a frame with border characters around the edges and
 * fills the center with text and spaces.
 *
 * @param buffer Pointer to buffer of exactly SCREEN_BUFFER_SIZE bytes.
 */
void generate_border_pattern(char *buffer);

